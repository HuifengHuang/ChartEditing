function safeJson(value) {
  try {
    return JSON.stringify(value, null, 2);
  } catch (error) {
    return "{}";
  }
}

export function buildExtractionPrompt({ intent, sourceData, renderCode }) {
  const outputSchema = `{
  "updatedSourceData": {},
  "updatedRenderCode": "string",
  "extractionMap": {
    "parameter_name": {
      "recommendation": ["preset1", "preset2", "preset3"],
      "primaryBindings": [
        {
          "name": "string",
          "type": "text|number|color|bool",
          "value": "any"
        }
      ],
      "expand": [
        {
          "name": "string",
          "type": "text|number|color|bool",
          "value": "any"
        }
      ],
      "affected": [
        {
          "name": "string",
          "type": "text|number|color|bool",
          "value": "any"
        }
      ],
      "data": [
        {}
      ],
      "input": [
        {
          "name": "image-x",
          "type": "number",
          "value": 0
        }
      ]
    }
  }
}`;

  return [
    "You are the Extraction stage in a controlled chart-editing pipeline.",
    "",
    "Your task is Extraction only.",
    "Do NOT generate UI.",
    "Do NOT generate PanelSpec.",
    "Do NOT explain.",
    "Do NOT output markdown.",
    "Do NOT output code fences.",
    "Return STRICT JSON only.",
    "",
    "You will receive:",
    "",
    "1. intent",
    "2. source_data",
    "3. render_script",
    "",
    "Intent semantics:",
    "",
    "1. intent.target can only be:",
    '   - "data"',
    '   - "style"',
    '   - "other"',
    "2. intent.action can only be:",
    '   - "add"',
    '   - "remove"',
    '   - "update"',
    "3. intent.expand only means:",
    "   - whether extra detailed controls are needed beyond the minimal predefined controls",
    "   - it does NOT decide whether extraction is needed",
    "4. intent.parameters is a dictionary:",
    "   - keys are generated by the previous stage",
    "   - values can only be:",
    '     - "Recommendation"',
    '     - "Preset"',
    '     - "None"',
    "5. Parameter value meanings:",
    '   - "Recommendation" means you must generate at least 3 preset schemes',
    '   - "Preset" means you must recover current values from existing code or current state, without inventing recommended values',
    '   - "None" is only for the upload-image special case',
    "6. Input special case:",
    '   - if intent.target == "other" and intent.parameters contains "Input": "None"',
    "   - then this is the SVG/image upload case",
    "   - return ONLY the fixed input fields",
    "",
    "Main goals:",
    "",
    "1. Identify all code fields relevant to the current intent",
    "2. Ensure all returned names exist in updatedSourceData",
    "3. If a field exists only in render_script, expose it into updatedSourceData",
    "4. If a field exists in neither source_data nor render_script but is required by the intent, create it in updatedSourceData",
    "5. Rewrite updatedRenderCode so that related hardcoded values are read from updatedSourceData",
    "6. Keep unrelated fields and code unchanged as much as possible",
    "7. Make the smallest possible code modification",
    "",
    "Output schema:",
    outputSchema,
    "",
    "Hard rules:",
    "",
    "1. All names returned in extractionMap.primaryBindings, extractionMap.expand, and extractionMap.affected must exist in updatedSourceData.",
    "2. Use stable field names. Prefer reusing existing names if possible.",
    "3. For newly created fields, use consistent snake_case names or stable dotted paths such as:",
    "   - layout.chart_width",
    "   - style.primary_color",
    "   - text.subtitle_color",
    "4. Only modify code that is directly related to the current intent.",
    "5. Do not rewrite unrelated rendering logic.",
    "6. Prefer minimal patching over large rewrites.",
    "7. If a field is already bound through source_data, reuse it instead of creating a duplicate field.",
    "8. Do not generate full HTML.",
    "9. Do not generate UI definitions.",
    "10. Do not generate PanelSpec.",
    "11. Do not output codePatches.",
    "12. Do not output bindingMap.",
    "",
    "Rules for primaryBindings and expand:",
    "",
    "1. primaryBindings and expand are mutually exclusive for each parameter.",
    "2. If intent.expand == false:",
    "   - return primaryBindings when applicable",
    "   - do not return expand for that parameter",
    "3. If intent.expand == true:",
    "   - return expand when applicable",
    "   - do not return primaryBindings for that parameter",
    "",
    "Rules for Recommendation:",
    "",
    '1. If a parameter value in intent.parameters is "Recommendation":',
    "   - you must return recommendation",
    "   - recommendation must contain at least 3 preset schemes",
    "2. recommendation is a simple list of preset labels or preset identifiers",
    "3. Recommendation is not a single suggested value",
    "4. Recommendation is a set of preset schemes",
    "5. If Recommendation is used:",
    "   - related fields in primaryBindings or expand must use value arrays",
    "   - each value array must have the same length as recommendation",
    "   - the values must correspond one-to-one with recommendation",
    "6. Example:",
    '   - recommendation = ["soft", "warm", "cool"]',
    "   - a related color field may have:",
    '     value = ["#d97c6c", "#c85b2a", "#5b7bd9"]',
    "",
    "Rules for Preset:",
    "",
    '1. If a parameter value in intent.parameters is "Preset":',
    "   - do not invent new recommended values",
    "   - recover current values from source_data or render_script",
    "   - expose only the fields needed for the current intent",
    "2. In Preset mode:",
    "   - primaryBindings.value or expand.value should be a single current value, not an array",
    "",
    "Rules for data editing:",
    "",
    '1. If intent.target == "data":',
    "   - for the related parameter, return ONLY:",
    "     - data",
    "     - affected",
    "   - do NOT return primaryBindings",
    "   - do NOT return expand",
    "   - do NOT return recommendation",
    "2. data must come from current code/state",
    "3. affected must include at least:",
    "   - width-related fields",
    "   - height-related fields",
    "   - spacing-related fields",
    "   - text content related fields",
    "4. Text content fields are mandatory in affected for data editing",
    "",
    "Rules for Input special case:",
    "",
    '1. If intent.target == "other" and intent.parameters contains "Input": "None":',
    "   - return ONLY input for that parameter",
    "   - do NOT return primaryBindings",
    "   - do NOT return expand",
    "   - do NOT return recommendation",
    "   - do NOT return data",
    "   - do NOT return affected",
    "2. input must be exactly:",
    "   [",
    '   {"name":"image-x","type":"number","value":0},',
    '   {"name":"image-y","type":"number","value":0},',
    '   {"name":"image-width","type":"number","value":100},',
    '   {"name":"image-height","type":"number","value":100}',
    "   ]",
    "",
    "Rules for affected fields beyond data editing:",
    "",
    "1. For aspect-ratio or chart-size related parameters:",
    "   - affected should include other layout fields likely impacted by width/height changes",
    "2. For color-theme related parameters:",
    "   - affected should include all related color fields that should stay coordinated",
    "3. For legend related parameters:",
    "   - affected should include legend shared fields and legend item-level fields when relevant",
    "",
    "Rules for code rewriting:",
    "",
    "1. If a related value is hardcoded in render_script, rewrite updatedRenderCode so that it reads from updatedSourceData.",
    "2. If a related value is already derived from source_data, keep that binding.",
    "3. If a related field does not exist anywhere but is needed, create it in updatedSourceData and bind updatedRenderCode to it.",
    "4. updatedRenderCode must be the final rewritten render code.",
    "",
    "Expected behavior examples:",
    "",
    "1. If the intent is about chart width and height:",
    "   - recover or expose width/height fields",
    "   - place them in updatedSourceData",
    "   - bind updatedRenderCode to them",
    "   - return primaryBindings when expand is false",
    "2. If the intent is about soft color themes:",
    "   - expose relevant color fields",
    "   - create at least 3 preset schemes",
    "   - return recommendation plus related fields",
    "3. If the intent is about triangle style:",
    "   - expose at least triangle base width, fill color, and white border width",
    "   - return 3 preset schemes if Recommendation is requested",
    "4. If the intent is about adding or removing data:",
    "   - return current editable data rows in data",
    "   - return affected including width, height, spacing, and text content",
    "5. If the intent is about uploading SVG:",
    "   - return only the fixed input fields",
    "",
    "intent:",
    safeJson(intent || {}),
    "",
    "source_data:",
    safeJson(sourceData || {}),
    "",
    "render_code:",
    String(renderCode || ""),
    "",
    "Return STRICT JSON only.",
  ].join("\n");
}
